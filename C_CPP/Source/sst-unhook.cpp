void Unhook_NtSysDebug(void)
{
	DWORD fNtSystemDebugControl = (DWORD)GetProcAddress(GetModuleHandle("ntdll"), "NtSystemDebugControl");
	if (fNtSystemDebugControl == 0)	return;
	SST_NtSystemDebugControl = *(PDWORD)(fNtSystemDebugControl + 1);

	if (ReadSST(sst))
	{
		if (call_r0((ULONG)&r0_restore_sst))
		{
			for (int i = 0; i < dwServices; i++)
			{
				if (old_sst[i] != sst[i])
				{
					// 
				}
			}
		}
	} 

}
