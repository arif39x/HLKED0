ULONGLONG ReadMsr(LONG MsrNum)
{ 
	MSR_STRUCT Msr = {0};
	Msr.MsrNum = MsrNum;

	NTSTATUS ns = Int2eNtSystemDebugControl(SysDbgSysReadMsr, &Msr, sizeof(Msr), NULL, 0, 0);
	if (!NT_SUCCESS(ns)) return 0;
		
	return Msr.MsrValue;
}
// ---------------------------------------------------------------------------------------
BOOL WriteMsr(LONG MsrNum, ULONGLONG Value)
{ 
	MSR_STRUCT Msr = {0};
	Msr.MsrNum = MsrNum;
	Msr.MsrValue = Value;

	NTSTATUS ns = Int2eNtSystemDebugControl(SysDbgSysWriteMsr, &Msr, sizeof(Msr), NULL, 0, 0);
	if (!NT_SUCCESS(ns)) return FALSE;

	return TRUE;
}

