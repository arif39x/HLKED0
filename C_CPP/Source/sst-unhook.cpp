// address of SST
DWORD sstaddr;
// number of services to restore
DWORD dwServices;
// SST-dumper
// ---------------------------------------------------------------------------------------
BOOL ReadSST(PDWORD psst)
{    
	PSYSTEM_MODULE_INFORMATION pModules = (PSYSTEM_MODULE_INFORMATION)&pModules;
	DWORD dwNeededSize = 0;
	
	dwServices = 0;

	NTSTATUS ns = NtQuerySystemInformation(SystemModuleInformation, pModules, 4, &dwNeededSize);
	if (ns == STATUS_INFO_LENGTH_MISMATCH) 
	{
		pModules = (PSYSTEM_MODULE_INFORMATION)VirtualAlloc(NULL, dwNeededSize, 
			MEM_RESERVE | MEM_COMMIT, PAGE_READWRITE);
		if (pModules == NULL) return FALSE;
        ns = NtQuerySystemInformation(SystemModuleInformation, pModules, dwNeededSize, NULL);
    }

	if (ns != STATUS_SUCCESS) return FALSE;
    
	DWORD dwKernelBase = pModules->aSM[0].Base;
	char *pKernelName = pModules->aSM[0].ModuleNameOffset + pModules->aSM[0].ImageName;
    
	HMODULE hKernel = LoadLibraryEx(pKernelName, 0, DONT_RESOLVE_DLL_REFERENCES);
	if (hKernel == NULL) return FALSE;

	VirtualFree(pModules, 0, MEM_RELEASE);

	DWORD dwKSDT;
	if (!(dwKSDT = (DWORD)GetProcAddress(hKernel, "KeServiceDescriptorTable")))	return FALSE;

	dwKSDT -= (DWORD)hKernel;
	DWORD dwKiServiceTable;
	if (!(dwKiServiceTable = FindKiServiceTable(hKernel, dwKSDT))) return FALSE;

	PIMAGE_FILE_HEADER		pfh;
	PIMAGE_OPTIONAL_HEADER	poh;
	PIMAGE_SECTION_HEADER	psh;
	GetHeaders((PCHAR)hKernel, &pfh, &poh, &psh);

	PDWORD pService = (PDWORD)((DWORD)hKernel + dwKiServiceTable);

	for (pService = (PDWORD)((DWORD)hKernel + dwKiServiceTable);
		*pService-poh->ImageBase < poh->SizeOfImage;
		pService++, dwServices++) 
	{
		psst[dwServices] = *pService - poh->ImageBase + dwKernelBase;
	}

	sstaddr = (DWORD)(dwKernelBase + dwKSDT); 

    FreeLibrary(hKernel);

	return TRUE;
} 
